@register.simple_tag(takes_context=True)
def masterpiece_trans(context, text):
    request = context.get('request')
    lang = request.session.get('masterpiece_lang', 'en') if request else 'en'
    
    if lang == 'en':
        return text
    
    return TRANSLATIONS.get(lang, {}).get(text, text)
